        // Accordion for job listings
        document.querySelectorAll('.job-row').forEach(row => {
            row.addEventListener('click', () => {
            const job = row.parentElement;
            const wasOpen = job.classList.contains('open');
            document.querySelectorAll('.job.open').forEach(j => j.classList.remove('open'));
            if(!wasOpen) job.classList.add('open');
            });
        });

        // Filters
        const filterBtns = document.querySelectorAll('.filter-btn');
        const jobs = document.querySelectorAll('.job');
        const noResults = document.getElementById('noResults');
        filterBtns.forEach(btn => {
            btn.addEventListener('click', () => {
            filterBtns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');
            const filter = btn.dataset.filter;
            let visibleCount = 0;
            jobs.forEach(job => {
                const show = filter === 'all' || job.dataset.cat === filter;
                job.style.display = show ? '' : 'none';
                if(show) visibleCount++;
                if(!show) job.classList.remove('open');
            });
            noResults.style.display = visibleCount === 0 ? 'block' : 'none';
            });
        });
    </script>
